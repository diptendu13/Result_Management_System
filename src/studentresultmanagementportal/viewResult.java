    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String roll = RollNumTextField.getText();
        Connection con=null;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/rms","root", "M@nd@l10du98");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student, result, course where student.roll='"+roll+"' and result.roll='"+roll+"' and result.cid=course.cid");
            if (rs.next()){
                // First fill all student details
                NameTextField.setText(rs.getString(5));
                CourseIdTextField.setText(rs.getString(11));
                CourseNameTextField.setText(rs.getString(2));
                SpecializationTextField.setText(rs.getString(3));
                SemesterTextField.setText(rs.getString(4));
                GenderTextField.setText(rs.getString(6));
                GuardianTextField.setText(rs.getString(7));
                EmailTextField.setText(rs.getString(9));
                
                // Then fill the paper names and their respective marks
                Paper1Label.setText(rs.getString(16));
                Paper2Label.setText(rs.getString(17));
                Paper3Label.setText(rs.getString(18));
                p1marksTextField.setText(rs.getString(12));
                p2marksTextField.setText(rs.getString(13));
                p3marksTextField.setText(rs.getString(14));
                
                // Then calculate the Total marks and Percentage upto 2 decimal places
                int p1 = Integer.parseInt(p1marksTextField.getText());
                int p2 = Integer.parseInt(p2marksTextField.getText());
                int p3 = Integer.parseInt(p3marksTextField.getText());
                int s = p1 + p2 + p3;
                String total = String.valueOf(s);
                TotalMarksTextField.setText(total);
                double avg = (s*100)/300.0;
                double p = Math.round(avg*100)/100.0; // dividing by 100.0 and not 100 for getting double data-type output
                String percentage = String.valueOf(p);
                PercentageTextField.setText(percentage+" %");
                
                // Then fill the grades for individual papers
                // Paper_1
                if (p1 >= 90){
                    p1GradeTextField.setText("O");
                }
                else if (p1 >= 80 && p1 < 90){
                    p1GradeTextField.setText("E");
                }
                else if (p1 >= 70 && p1 < 80){
                    p1GradeTextField.setText("A");
                }
                else if (p1 >= 60 && p1 < 70){
                    p1GradeTextField.setText("B");
                }
                else if (p1 >= 50 && p1 < 60){
                    p1GradeTextField.setText("C");
                }
                else if (p1 >= 40 && p1 < 50){
                    p1GradeTextField.setText("D");
                }
                else{
                    p1GradeTextField.setText("F");
                }
                // Paper_2
                if (p2 >= 90){
                    p2GradeTextField.setText("O");
                }
                else if (p2 >= 80 && p2 < 90){
                    p2GradeTextField.setText("E");
                }
                else if (p2 >= 70 && p2 < 80){
                    p2GradeTextField.setText("A");
                }
                else if (p2 >= 60 && p2 < 70){
                    p2GradeTextField.setText("B");
                }
                else if (p2 >= 50 && p2 < 60){
                    p2GradeTextField.setText("C");
                }
                else if (p2 >= 40 && p2 < 50){
                    p2GradeTextField.setText("D");
                }
                else{
                    p2GradeTextField.setText("F");
                }
                // Paper_3
                if (p3 >= 90){
                    p3GradeTextField.setText("O");
                }
                else if (p3 >= 80 && p3 < 90){
                    p3GradeTextField.setText("E");
                }
                else if (p3 >= 70 && p3 < 80){
                    p3GradeTextField.setText("A");
                }
                else if (p3 >= 60 && p3 < 70){
                    p3GradeTextField.setText("B");
                }
                else if (p3 >= 50 && p3 < 60){
                    p3GradeTextField.setText("C");
                }
                else if (p3 >= 40 && p3 < 50){
                    p3GradeTextField.setText("D");
                }
                else{
                    p3GradeTextField.setText("F");
                }
                
                // Now fill the final grade and remarks by seeing the percentage
                if (p1 < 40 || p2 < 40 || p3 < 40){  // student needs to pass in individual subjects as well
                    TotalGradeTextField.setText("F");
                    RemarksTextField.setText("Failed");
                }
                else if (p >= 90){
                    TotalGradeTextField.setText("O");
                    RemarksTextField.setText("Outstanding!");
                }
                else if (p >= 80 && p < 90){
                    TotalGradeTextField.setText("E");
                    RemarksTextField.setText("Excellent!");
                }
                else if (p >= 70 && p < 80){
                    TotalGradeTextField.setText("A");
                    RemarksTextField.setText("Very Good!");
                }
                else if (p >= 60 && p < 70){
                    TotalGradeTextField.setText("B");
                    RemarksTextField.setText("Good!");
                }
                else if (p >= 50 && p < 60){
                    TotalGradeTextField.setText("C");
                    RemarksTextField.setText("Fair");
                }
                else if (p >= 40 && p < 50){
                    TotalGradeTextField.setText("D");
                    RemarksTextField.setText("Below Average");
                }
                
                // Finally, fill the Result Status
                if (p1 < 40 || p2 < 40 || p3 < 40){ // student needs to pass in individual subjects as well
                    ResultStatusTextField.setText("Failed");
                }
                else if (p >= 90){
                    ResultStatusTextField.setText("Passed with Distinction");
                }
                else if (p >= 75 && p < 90){
                    ResultStatusTextField.setText("Passed with Higher Merit");
                }
                else if (p >= 55 && p < 75){
                    ResultStatusTextField.setText("Passed with Merit");
                }
                else if (p >= 40 && p < 55){
                    ResultStatusTextField.setText("Passed");
                    
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

