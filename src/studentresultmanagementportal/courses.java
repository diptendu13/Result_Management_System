    private void AddCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddCourseButtonActionPerformed
        // TODO add your handling code here:
        String cid = CourseIdTextField.getText();
        String paper1 = Paper1TextField.getText();
        String paper2 = Paper2TextField.getText();
        String paper3 = Paper3TextField.getText();
        
        Connection con = null;
        boolean field = false;
        if(cid.equals("")){
            JOptionPane.showMessageDialog(null,"Course ID Required");
            field = true;
        }
        else if(paper1.equals("")){
            JOptionPane.showMessageDialog(null,"Paper_1 Required");
            field = true;
        }
        else if(paper2.equals("")){
            JOptionPane.showMessageDialog(null,"Paper_2 Required");
            field = true;
        }
        else if(paper3.equals("")){
            JOptionPane.showMessageDialog(null,"Paper_3 Required");
            field = true;
        }
        if (field == false){
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/rms","root", "M@nd@l10du98");
           Statement st = con.createStatement();
           ResultSet rs = st.executeQuery("select * from course where cid='"+cid+"'");
           if (rs.next()){
               JOptionPane.showMessageDialog(null, "Course ID already exists");
           }
           else{
               st.executeUpdate("insert into course values('"+cid+"','"+paper1+"','"+paper2+"','"+paper3+"')");
               JOptionPane.showMessageDialog(null, "Successfully Added");
               setVisible(false);
               new courses().setVisible(true);
           }  
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        }
    }//GEN-LAST:event_AddCourseButtonActionPerformed

