    private void InsertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertButtonActionPerformed
        // TODO add your handling code here:
        String roll = RollNumTextField.getText();
        String cid = CourseIdTextField.getText();
        String p1marks = Paper1TextField.getText();
        String p2marks = Paper2TextField.getText();
        String p3marks = Paper3TextField.getText();
        
        Connection con = null;
        boolean field = false;
        
        if(roll.equals("")){
            JOptionPane.showMessageDialog(null,"Roll Number Required");
            field = true;
        }
        else if(cid.equals("")){
            JOptionPane.showMessageDialog(null,"Course ID Required");
            field = true;
        }
        else if(p1marks.equals("")){
            JOptionPane.showMessageDialog(null,"Paper_1 Marks Required");
            field = true;
        }
        else if(p2marks.equals("")){
            JOptionPane.showMessageDialog(null,"Paper_2 Marks Required");
            field = true;
        }
        else if(p3marks.equals("")){
            JOptionPane.showMessageDialog(null,"Paper_3 Marks Required");
            field = true;
        }
        else if((Integer.parseInt(p1marks) > 100) || (Integer.parseInt(p2marks) > 100) || (Integer.parseInt(p3marks) > 100)){
            JOptionPane.showMessageDialog(null,"Please enter marks in range 0 to 100");
            field = true;
        }
        if (field == false){
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/rms","root", "M@nd@l10du98");
           Statement st = con.createStatement();
           ResultSet rs = st.executeQuery("select * from student where roll='"+roll+"'");
           if (rs.next()){
               st.executeUpdate("insert into result values('"+roll+"','"+cid+"','"+p1marks+"','"+p2marks+"','"+p3marks+"')");
               JOptionPane.showMessageDialog(null, "Successfully Inserted");
               setVisible(false);
               new insertNewResult().setVisible(true);
           }
           else{
               JOptionPane.showMessageDialog(null, "Invalid Roll Number");
           }  
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Entry to corresponding roll number already exists. Can't be inserted.");
        }
        }
    }//GEN-LAST:event_InsertButtonActionPerformed

    private void Paper1TextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Paper1TextFieldKeyPressed
        // TODO add your handling code here:
        int numLength = Paper1TextField.getText().length();
        
        char c = evt.getKeyChar();
        
        if (Character.isDigit(c)){
            if (numLength < 3){
                Paper1TextField.setEditable(true);
            }
            else{
                Paper1TextField.setEditable(false);
            }
        }
        else{
            if (evt.getExtendedKeyCode() == java.awt.event.KeyEvent.VK_BACK_SPACE){
                Paper1TextField.setEditable(true);
            }
            else{
                Paper1TextField.setEditable(false);
            } 
        }
    }//GEN-LAST:event_Paper1TextFieldKeyPressed

